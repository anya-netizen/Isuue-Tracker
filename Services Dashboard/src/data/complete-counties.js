// Utility functions for the complete dataset
export const getCompleteCountiesByState = (state) => {
  return COMPLETE_US_COUNTIES.filter(county => county.state === state);
};

export const getAllCompleteStates = () => {
  return [...new Set(COMPLETE_US_COUNTIES.map(county => county.state))].sort();
};

export const searchCompleteCounties = (searchTerm) => {
  if (!searchTerm) return COMPLETE_US_COUNTIES;
  const term = searchTerm.toLowerCase();
  return COMPLETE_US_COUNTIES.filter(county => 
    county.name.toLowerCase().includes(term) || 
    county.state.toLowerCase().includes(term)
  );
};

export const getCountiesByFips = (fipsCode) => {
  return COMPLETE_US_COUNTIES.find(county => county.fips === fipsCode);
};

export const getCountiesByPopulation = (minPopulation = 0, maxPopulation = Infinity) => {
  return COMPLETE_US_COUNTIES.filter(county => 
    county.population >= minPopulation && county.population <= maxPopulation
  );
};

// Summary statistics
export const getDatasetStats = () => {
  const totalCounties = COMPLETE_US_COUNTIES.length;
  const totalStates = getAllCompleteStates().length;
  const totalPopulation = COMPLETE_US_COUNTIES.reduce((sum, county) => sum + county.population, 0);
  const avgPopulation = Math.round(totalPopulation / totalCounties);
  
  return {
    totalCounties,
    totalStates,
    totalPopulation,
    avgPopulation,
    largestCounty: COMPLETE_US_COUNTIES.reduce((max, county) => 
      county.population > max.population ? county : max, COMPLETE_US_COUNTIES[0]),
    smallestCounty: COMPLETE_US_COUNTIES.reduce((min, county) => 
      county.population < min.population ? county : min, COMPLETE_US_COUNTIES[0])
  };
};