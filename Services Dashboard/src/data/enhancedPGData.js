// Generate comprehensive PG data with realistic variations
export const generateEnhancedPGData = () => {
  const specialties = [
    "Internal Medicine", "Family Practice", "Cardiology", "Endocrinology",
    "Geriatrics", "Pulmonology", "Nephrology", "Oncology", "Rheumatology",
    "Neurology", "Gastroenterology", "Infectious Disease"
  ];

  const performanceVariations = [
    { range: 'high', min: 80, max: 95, weight: 0.2 },
    { range: 'good', min: 65, max: 79, weight: 0.4 },
    { range: 'medium', min: 45, max: 64, weight: 0.3 },
    { range: 'low', min: 25, max: 44, weight: 0.1 }
  ];

  return USPGLocations.map((location, index) => {
    // Determine performance level
    const rand = Math.random();
    let performance;
    let cumWeight = 0;
    
    for (const variant of performanceVariations) {
      cumWeight += variant.weight;
      if (rand <= cumWeight) {
        performance = variant;
        break;
      }
    }
    
    const baseScore = Math.floor(Math.random() * (performance.max - performance.min + 1)) + performance.min;
    const patientCount = Math.floor(Math.random() * 80) + 20; // 20-100 patients
    const avgRevenue = Math.floor((baseScore / 100) * 60000) + Math.floor(Math.random() * 20000); // $10k-$80k
    const billabilityScore = baseScore + Math.floor(Math.random() * 20) - 10; // Â±10 from base
    
    return {
      id: `pg-${index + 1}`,
      name: location.name,
      npi: `${1000000000 + index}`,
      address: {
        street: `${Math.floor(Math.random() * 9999) + 1} ${location.name.split(' ')[0]} St`,
        city: location.city,
        state: location.state,
        zip: `${Math.floor(Math.random() * 90000) + 10000}`,
      },
      coordinates: {
        lat: location.lat + (Math.random() - 0.5) * 0.1, // Add slight variation
        lng: location.lng + (Math.random() - 0.5) * 0.1
      },
      phone: `(${Math.floor(Math.random() * 900) + 100}) ${Math.floor(Math.random() * 900) + 100}-${Math.floor(Math.random() * 9000) + 1000}`,
      email: `contact@${location.name.toLowerCase().replace(/[^a-z0-9]/g, '')}.com`,
      specialties: [
        specialties[Math.floor(Math.random() * specialties.length)],
        specialties[Math.floor(Math.random() * specialties.length)]
      ].filter((v, i, a) => a.indexOf(v) === i), // Remove duplicates
      activePatients: patientCount,
      totalRevenue: avgRevenue,
      billablePatients: Math.floor(patientCount * (billabilityScore / 100)),
      unbillablePatients: patientCount - Math.floor(patientCount * (billabilityScore / 100)),
      region: location.region,
      created_date: new Date(Date.now() - Math.random() * 365 * 24 * 60 * 60 * 1000).toISOString(),
      updated_date: new Date().toISOString(),
      hha_partnerships: [
        {
          id: `hha-${Math.floor(Math.random() * 20) + 1}`,
          name: `${location.city} Home Health Services`,
          npi: `${1700000000 + index}`,
          type: "Primary Partner",
          patients_count: Math.floor(patientCount * 0.4),
          monthly_revenue: Math.floor(avgRevenue * 0.4),
          partnership_start: new Date(Date.now() - Math.random() * 730 * 24 * 60 * 60 * 1000).toISOString().split('T')[0]
        },
        {
          id: `hha-${Math.floor(Math.random() * 20) + 21}`,
          name: `${location.state} Care Network`,
          npi: `${1800000000 + index}`,
          type: "Secondary Partner", 
          patients_count: Math.floor(patientCount * 0.3),
          monthly_revenue: Math.floor(avgRevenue * 0.3),
          partnership_start: new Date(Date.now() - Math.random() * 365 * 24 * 60 * 60 * 1000).toISOString().split('T')[0]
        }
      ]
    };
  });
};

